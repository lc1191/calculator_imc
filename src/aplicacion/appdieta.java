    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        
        //Mostar todos los alimentos en tabla
     DefaultTableModel modelo = new DefaultTableModel();
    
         // Creamos las columnas.
         modelo.addColumn("Tipo");
         modelo.addColumn("Nombre");
         modelo.addColumn("Calorias");
         modelo.addColumn("Carbohidratos");
         modelo.addColumn("Proteinas");
         modelo.addColumn("Grasas");
        
    try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/proyectocj","root","");
                String sql = "select * from alimentos";
                Statement sentencia =conexion.createStatement();
                ResultSet resultado = sentencia.executeQuery(sql);

        while(resultado.next()){
           //Array para crear columnas
                Object [] datos = new Object[6];
                for (int i = 0; i < 6; i++) {
                    datos[i] = resultado.getObject(i+1);
                }
                modelo.addRow(datos);
              }

        tabla1.setModel(modelo);
                
        tabla1.getColumn("Tipo").setPreferredWidth(120);
        tabla1.getColumn("Nombre").setPreferredWidth(230);
        tabla1.getColumn("Calorias").setPreferredWidth(100);
        tabla1.getColumn("Carbohidratos").setPreferredWidth(100);
        tabla1.getColumn("Proteinas").setPreferredWidth(100);
        tabla1.getColumn("Grasas").setPreferredWidth(100);
           
        sentencia.close();
                conexion.close();
                
        } catch (SQLException | ClassNotFoundException ex) 
        {
            Logger.getLogger(appdieta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_mostrarActionPerformed

    private void bminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bminActionPerformed
      
        //Mostrar dietas segun cantidad introducida
        basedatos b = new basedatos();
        try {
            txtarea.setText(b.listar(Double.parseDouble(calmin.getText())));
            txtarea.append("");
        } catch (NumberFormatException e) 
        {
            JOptionPane.showMessageDialog(null, "Introduzca una cantidad valida", "Error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(appdieta.class.getName()).log(Level.SEVERE,null,e);
        }
        
    }//GEN-LAST:event_bminActionPerformed

    private void mdietasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mdietasActionPerformed
       
        //Mostrar dietas existentes   
        basedatos b = new basedatos();
        try {
            txtarea.append("");
            txtarea.setText(b.listardietas());
        } catch (NumberFormatException e) 
        {
            JOptionPane.showMessageDialog(null, "No es posible conectar con base de datos", "Error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(appdieta.class.getName()).log(Level.SEVERE,null,e);
        }
        
    }//GEN-LAST:event_mdietasActionPerformed

